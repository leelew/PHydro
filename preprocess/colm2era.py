def h2osoi_colm2era(h2osoi):
    # turn h2osoi to ERA5-Land soil depth
    # NOTE: 0.71/2.79/6.23/11.89/21.22/36.61/61.98/103.80/172.76/286.46cm
    #       to 7/28/100/286.46cm
    # h2osoi: (time, lat, lon, soil) - swvl1 (time, lat, lon)
    swvl1 = (h2osoi[:, :, :, 0]*0.71+h2osoi[:, :, :, 1]*(2.79-0.71) +
             h2osoi[:, :, :, 2]*(6.23-2.79)+h2osoi[:, :, :, 3]*(7-6.23))/7
    swvl2 = (h2osoi[:, :, :, 3]*(11.89-7)+h2osoi[:, :, :, 4]*(21.22-11.89) +
             h2osoi[:, :, :, 5]*(28-21.22))/(28-7)
    swvl3 = (h2osoi[:, :, :, 5]*(36.61-28)+h2osoi[:, :, :, 6]*(61.98-36.61) +
             h2osoi[:, :, :, 7]*(100-61.98))/(100-28)
    swvl4 = (h2osoi[:, :, :, 7]*(103.80-100)+h2osoi[:, :, :, 8]*(172.76-103.80) +
             h2osoi[:, :, :, 9]*(286.46-172.76))/(286.46-100)
    return swvl1, swvl2, swvl3, swvl4
